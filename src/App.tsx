import { HashRouter, Route, Routes } from "react-router-dom";
import Header from "./components/Header/Header";

import Home from "./pages/Home";

export function App() {
  return (
    <>
      <Header />
      <Routes>
        <Route path="/" element={<Home />} />
      </Routes>
    </>
  );
}

function WrappedApp() {
  return (
    <HashRouter>
      <App />
    </HashRouter>
  );
}

export default WrappedApp;
